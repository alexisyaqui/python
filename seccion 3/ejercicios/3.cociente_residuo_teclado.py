
numero1 = float(input("ingrese el primer numero \t"))
numero2 = float(input("ingrese el segundo numero \t"))

float = resultado_cociente = numero1 // numero2
float = resultado_residuo = numero1 % numero2

print (f"el resultado del cociente es:  \t", +resultado_cociente)
print(f"el resultado del residuo es:  \t", +resultado_residuo)